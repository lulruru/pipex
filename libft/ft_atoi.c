#include "libft.h"

int	ft_atoi(const char *str)
{
	int	sign;
	int	r;
	int	i;

	sign = 0;
	i = 0;
	r = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + (str[i] - 48);
		i++;
	}
	if (sign % 2 == 1)
		return (-r);
	return (r);
}
